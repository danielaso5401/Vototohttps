class Candidato {
  String name;
  String dni;
  String partido;
  String fotocand;
  String fotopart;
  int id;

  Candidato(name, dni, partido, fotocand, fotopart, id) {
    this.name = name;
    this.dni = dni;
    this.partido = partido;
    this.fotocand = fotocand;
    this.fotopart = fotopart;
    this.id = id;
  }
}
